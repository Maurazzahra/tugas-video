let a = true;
let b = false;
let c = true;
let d = false;

// OR
// akan bernilai true jika salah satu nilai booleannya true
console.log(c || b || d);

// AND
// akan bernilai false jika salah satu false
console.log(a && b);

let x = 11;
let y = 9;

// lebih besar
console.log(x > y);

// lebih kecil
console.log(x < y);

// bernilai sama
console.log(x == y);

// lebih besar sama dengan
console.log(x >= y);

// lebih kecil sama dengan
console.log(x <= y);

let lima = "5";
let limo = 5;

// identik - sama tipe dan isi
console.log(lima === limo); // === disebut identik